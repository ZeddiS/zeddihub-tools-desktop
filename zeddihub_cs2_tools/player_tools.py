def draw_crosshair_ascii(size, gap, thickness, color_idx, dot):
    try:
        s = max(0, int(float(size)))
        g = int(float(gap))
        th = max(1, int(float(thickness) * 2))
        has_dot = int(float(dot)) == 1
    except Exception:
        s, g, th, has_dot = 2, 0, 1, False

    colors = [RED, GREEN, YELLOW, "\033[38;5;21m", CYAN, RESET]
    try:
        c_color = colors[int(color_idx)] if 0 <= int(color_idx) <= 5 else GREEN
    except Exception:
        c_color = GREEN

    art = []
    art.append(GRAY + "+" + "-" * 30 + "+" + RESET)
    center_y, center_x = 7, 15
    for y in range(15):
        row = ""
        for x in range(30):
            is_dot = has_dot and abs(x - center_x) <= 0 and abs(y - center_y) <= 0
            is_top = center_x - (th // 2) <= x <= center_x + (th // 2) and (center_y - g - s <= y < center_y - g)
            is_bot = center_x - (th // 2) <= x <= center_x + (th // 2) and (center_y + g < y <= center_y + g + s)
            is_left = center_y - (th // 2) <= y <= center_y + (th // 2) and (center_x - g - s * 2 <= x < center_x - g)
            is_right = center_y - (th // 2) <= y <= center_y + (th // 2) and (center_x + g < x <= center_x + g + s * 2)
            if is_dot:
                row += c_color + "#" + RESET
            elif is_top or is_bot or is_left or is_right:
                row += c_color + "|" + RESET
            else:
                row += " "
        art.append(GRAY + "|" + RESET + row + GRAY + "|" + RESET)
    art.append(GRAY + "+" + "-" * 30 + "+" + RESET)
    return art



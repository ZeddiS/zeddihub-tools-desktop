def draw_viewmodel_ascii(fov, x, y, z):
    """Draw ASCII art viewmodel preview based on current settings."""
    try:
        fov_f = float(fov)
        x_f = float(x)
        y_f = float(y)
        z_f = float(z)
    except Exception:
        fov_f, x_f, y_f, z_f = 68.0, 2.5, 0.0, -1.5

    art = []
    w, h = 40, 14

    # Compute gun position based on offsets
    gun_x = int(w * 0.6 + x_f * 2 - (fov_f - 68) * 0.3)
    gun_y = int(h * 0.7 + z_f * 1.5)
    gun_x = max(4, min(w - 6, gun_x))
    gun_y = max(3, min(h - 2, gun_y))

    grid = [[' ' for _ in range(w)] for _ in range(h)]

    # Draw gun body (simplified rifle shape)
    barrel_len = max(3, int(8 - (fov_f - 54) * 0.1))
    for bx in range(gun_x - barrel_len, gun_x + 1):
        if 0 <= bx < w and 0 <= gun_y < h:
            grid[gun_y][bx] = '='
        if 0 <= bx < w and 0 <= gun_y - 1 < h:
            grid[gun_y - 1][bx] = '-'

    # Grip
    for gy in range(gun_y + 1, min(h, gun_y + 4)):
        if 0 <= gun_x < w:
            grid[gy][gun_x] = '|'
        if 0 <= gun_x - 1 < w:
            grid[gy][gun_x - 1] = '|'

    # Stock
    for sx in range(gun_x + 1, min(w, gun_x + 5)):
        if 0 <= gun_y < h:
            grid[gun_y][sx] = '='
        if 0 <= gun_y + 1 < h:
            grid[gun_y + 1][sx] = '_'

    # Muzzle
    if 0 <= gun_x - barrel_len - 1 < w and 0 <= gun_y < h:
        grid[gun_y][gun_x - barrel_len - 1] = '>'

    # Crosshair in center
    cx, cy = w // 2, h // 3
    if 0 <= cy < h and 0 <= cx < w:
        grid[cy][cx] = '+'

    art.append(GRAY + "+" + "-" * w + "+" + RESET)
    for row in grid:
        line = ""
        for ch in row:
            if ch in ('=', '-', '|', '_', '>'):
                line += CYAN + ch + RESET
            elif ch == '+':
                line += GREEN + ch + RESET
            else:
                line += ch
        art.append(GRAY + "|" + RESET + line + GRAY + "|" + RESET)
    art.append(GRAY + "+" + "-" * w + "+" + RESET)
    info = f"FOV:{fov_f:.0f} X:{x_f:+.1f} Y:{y_f:+.1f} Z:{z_f:+.1f}"
    art.append(DIM + GRAY + center(info, w + 2) + RESET)
    return art



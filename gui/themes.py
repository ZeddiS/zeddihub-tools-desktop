def get_theme(game: str, mode: str = None) -> dict:
    base = dict(GAME_THEMES.get(game, GAME_THEMES["default"]))
    if mode == "light":
        light_overrides = base.pop("light", {})
        base.update(light_overrides)
    else:
        base.pop("light", None)
    return base
